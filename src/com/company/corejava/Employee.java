package com.company.corejava;

public class Employee {
    int employeeId;
    String employeeName;

    public void setEmployeeId(int empId){
        employeeId = empId;
    }
    public void setEmployeeName(String empName){
        employeeName = empName;
    }
    public int getEmployeeId(){
       return employeeId;
    }
    public String getEmployeeName(){
        return employeeName;
    }

}
